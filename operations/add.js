export function sum(num1, num2) {
    return num1 + num2;
}
export function minus(num1, num2) {
    return num1 - num2;
}
export function multi(num1, num2) {
    return num1 * num2;
}
export function divi(num1, num2) {
    return num1 / num2;
}
